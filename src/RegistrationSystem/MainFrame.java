    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(txtUsername.getText() == null && txtPassword.getText() == null){
            JOptionPane.showMessageDialog(this, "Please enter empty fields!", "Login", 1, null);
        }else{
            String user = txtUsername.getText();
            String pswd = txtPassword.getText();
            int x = dbOps.checkLogin(user, pswd);
            
            if(x==1){
                //JOptionPane.showMessageDialog(this, "Login successful!", "Login", 1, null);
                ProfileFrame pr = new ProfileFrame(user);
                pr.setVisible(true);
            }else if(x==0){
                JOptionPane.showMessageDialog(this, "Username or password mistmatched!", "Login", 0, null);
            }else{
                JOptionPane.showMessageDialog(this, "Error occured while checking username and password", "Login", 0, null);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

